a = 1
a = a + 2
print(a) # will print 3

a = 1
a += 2 # this is the augmented assignment operator
print(a) # will also print 3

word = "race"
word = word + " car"
print(word) # race car

word = "race"
word += " car"
print(word) # race car

b = 5
b = b * 3
print(b) # 15

b = 5 
b *= 3
print(b) # 15

